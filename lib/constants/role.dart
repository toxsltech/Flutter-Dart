String getRole(role) {
  if (role == "1")
    return "Legal";
  else if (role == "2")
    return "Technology";
  else if (role == "3")
    return "IT";
  else if (role == "4")
    return "Medical";
  else if (role == "5")
    return "Entrepreneur";
  else if (role == "6")
    return "Business";
  else if (role == "7")
    return "Education";
  else if (role == "8")
    return "Financial services";
  else if (role == "9")
    return "Management/ Leadership";
  else if (role == "10")
    return "Personal development/ coaching";
  else if (role == "11")
    return "Health/ Nutrition";
  else if (role == "12")
    return "Other";
  else if (role == "13")
    return "RS";
  else
    return "";
}
